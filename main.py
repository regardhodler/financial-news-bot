# ─────────────────────────────────────────────────────────────────────────────
# XCLID PATCH — Fix twscrape's XClientTxId generation.
#
# X's API requires an "X-Client-Transaction-Id" header generated by parsing
# x.com's JavaScript chunk map and SVG animations. twscrape 0.17.0 uses
# json.loads() to parse the chunk map, but x.com changed to JavaScript object
# literal syntax (unquoted keys), which is valid JS but not valid JSON.
#
# This patch replaces the JSON parser with a regex extractor that handles
# both quoted and unquoted keys, fixing "Failed to parse scripts" everywhere.
# ─────────────────────────────────────────────────────────────────────────────

def _setup_xclid_patch() -> None:
    """
    Patch twscrape's get_scripts_list() to handle x.com's frequently-changing
    webpack bundle format.

    Tries three strategies in order:
      1. Original pattern (e=>e+"."+) — webpack 4 style
      2. Newer webpack 5 / x.com style: {map}[e]||e  variants
      3. Direct regex scan for ondemand.s.* URLs in the page text (no chunk map needed)
    """
    import twscrape.xclid as _xclid

    _HASH_PAT = re.compile(r'"?([^":\s{},\[\]]+)"?\s*:\s*"([a-f0-9]{5,10})"')

    def _extract_from_chunk(chunk_text: str):
        for key, val in _HASH_PAT.findall(chunk_text):
            yield _xclid.script_url(key, f"{val}a")

    def _patched_get_scripts_list(text: str):
        # Strategy 1: original webpack 4 pattern (e=>e+"."+{...}[e]+"a.js")
        if 'e=>e+"."+' in text and '[e]+"a.js"' in text:
            try:
                chunk = text.split('e=>e+"."+')[1].split('[e]+"a.js"')[0]
                results = list(_extract_from_chunk(chunk))
                if results:
                    log.info("[XClId] Strategy 1 (webpack4 chunk map) matched.")
                    yield from results
                    return
            except Exception:
                pass

        # Strategy 2: webpack 5 / newer x.com — {map}[e]||e or e=>({...}[e]||e)
        for marker_start, marker_end in [
            (r'\{[^{}]{40,}\}\[e\]\|\|e', None),
            (r'chunksById', None),
        ]:
            try:
                m = re.search(r'\{([^{}]{40,})\}\[e\]', text)
                if m:
                    results = list(_extract_from_chunk(m.group(1)))
                    if results:
                        log.info("[XClId] Strategy 2 (webpack5 chunk map) matched.")
                        yield from results
                        return
            except Exception:
                pass

        # Strategy 3: direct URL scan — find ondemand.s.* script URLs in the page
        direct = re.findall(
            r'https://abs\.twimg\.com/responsive-web/client-web/ondemand\.s\.[a-f0-9a-z]+\.js',
            text,
        )
        if direct:
            log.info(f"[XClId] Strategy 3 (direct URL scan) matched {len(direct)} URLs.")
            yield from dict.fromkeys(direct)  # deduplicate, preserve order
            return

        # Strategy 4: scan all abs.twimg.com client-web JS chunks
        all_chunks = re.findall(
            r'https://abs\.twimg\.com/responsive-web/client-web/[a-zA-Z0-9._/-]+\.js',
            text,
        )
        if all_chunks:
            log.warning(f"[XClId] Strategy 4 (all chunks fallback): yielding {len(all_chunks)} URLs.")
            yield from dict.fromkeys(all_chunks)
            return

        log.error("[XClId] All strategies failed — x.com page format unrecognised.")

    _xclid.get_scripts_list = _patched_get_scripts_list

    # Also patch parse_anim_idx — it filters scripts for "/ondemand.s." which may
    # not appear in the URLs returned by our fallback strategies. Instead, try ALL
    # scripts and return indices from the first one that contains them.
    async def _patched_parse_anim_idx(text: str) -> list[int]:
        scripts = list(_xclid.get_scripts_list(text))
        if not scripts:
            raise Exception("Couldn't get XClientTxId scripts")

        # Prefer ondemand.s. scripts (original behavior), then fall back to others
        ordered = (
            [s for s in scripts if "/ondemand.s." in s]
            + [s for s in scripts if "/ondemand.s." not in s]
        )

        for url in ordered[:10]:
            try:
                js_text = await _xclid.get_tw_page_text(url)
                items = [int(m.group(2)) for m in _xclid.INDICES_REGEX.finditer(js_text)]
                if items:
                    log.info(f"[XClId] Found animation indices in {url.split('/')[-1]}")
                    return items
            except Exception:
                continue

        raise Exception("Couldn't get XClientTxId indices from any available script")

    _xclid.parse_anim_idx = _patched_parse_anim_idx
    log.info("[XClId] Patched get_scripts_list() + parse_anim_idx() with fallback chain.")


