# MAIN ORCHESTRATION
# ─────────────────────────────────────────────────────────────────────────────

async def main():
    log.info("=" * 60)
    log.info("Financial News Bot starting up")
    log.info(f"Config: MIN_IMPACT={MIN_IMPACT_SCORE} | MAX_ALERTS={MAX_ALERTS} | LOOKBACK={LOOKBACK_MINUTES}min")
    log.info("=" * 60)

    # ── Step 0: Market Snapshot (every 4 hours only) ────────────────────────
    snapshot = None
    current_hour = datetime.now(timezone.utc).hour
    if current_hour % 4 == 0:
        snapshot = get_market_snapshot()
        if snapshot:
            log.info("[Snapshot] Market data fetched successfully.")
            send_telegram(snapshot)
            send_discord(snapshot)
        else:
            log.info("[Snapshot] Skipped (no data available).")
    else:
        log.info(f"[Snapshot] Skipped (next snapshot at hour {((current_hour // 4) + 1) * 4 % 24}:00 UTC).")

    # ── Step 0b: FinViz Signals (hourly only to avoid rate limiting) ────────
    finviz_data = None
    current_minute = datetime.now(timezone.utc).minute
    if current_minute < 15:  # Only on the first run of each hour
        finviz_data = scrape_finviz_signals()
    finviz_context = finviz_data["context_block"] if finviz_data else None
    if finviz_context:
        log.info(f"[FinViz] Context block ready ({len(finviz_context)} chars).")
    else:
        log.info("[FinViz] No FinViz data available — scoring will proceed without cross-validation.")

    # ── Step 1: Validate required secrets ─────────────────────────────────────
    groq_api_key = os.getenv("GROQ_API_KEY", "").strip()
    if not groq_api_key:
        log.error("GROQ_API_KEY not set. Aborting.")
        return
    groq_client = Groq(api_key=groq_api_key)

    # ── Step 2: Patch XClIdGen + Login to X accounts ──────────────────────────
    _setup_xclid_patch()
    api = API()
    accounts = await login_accounts(api)
    if accounts == 0:
        log.error("No X accounts available. Aborting.")
        return

    # ── Step 3: Scrape tweets ──────────────────────────────────────────────────
    tweets = await scrape_tweets(api)
    if not tweets:
        log.info("[Main] No tweets found in lookback window — skipping scoring/alerts.")
        # Still fetch Polymarket and write Gist snapshot even with no tweets
        polymarket_data = fetch_polymarket_markets()
        write_to_gist(narrative=None, top_alerts=[], polymarket=polymarket_data)
        log.info("=" * 60)
        return

    # ── Step 4: Batch-score with Groq, send qualifying alerts ──────────────
    alerts_sent = 0
    gist_alerts: list[dict] = []  # Collect for Gist snapshot
    total_batches = (len(tweets) + BATCH_SIZE - 1) // BATCH_SIZE
    log.info(f"[Main] Scoring {len(tweets)} tweets in {total_batches} batches of {BATCH_SIZE}.")

    for batch_idx in range(0, len(tweets), BATCH_SIZE):
        if alerts_sent >= MAX_ALERTS:
            log.info(f"[Main] Reached MAX_ALERTS ({MAX_ALERTS}). Stopping.")
            break

        batch = tweets[batch_idx : batch_idx + BATCH_SIZE]
        batch_num = batch_idx // BATCH_SIZE + 1
        log.info(f"[Main] Scoring batch {batch_num}/{total_batches} ({len(batch)} tweets)...")

        # Rate-limit pause before Groq call
        await asyncio.sleep(GROQ_RATE_LIMIT_SLEEP)

        scores = batch_score_tweets(batch, groq_client, finviz_context=finviz_context)

        for tweet, score in zip(batch, scores):
            if alerts_sent >= MAX_ALERTS:
                break

            if score is None:
                log.info(f"[Main] Skipping tweet {tweet['id']} — Groq returned no result.")
                continue

            if score["impact"] < MIN_IMPACT_SCORE:
                log.info(
                    f"[Main] Skipping tweet {tweet['id']} — "
                    f"impact {score['impact']}/10 < threshold {MIN_IMPACT_SCORE}."
                )
                continue

            # Build and send alert
            message = build_alert_message(tweet, score)
            log.info(f"[Main] Sending alert for tweet {tweet['id']} (impact {score['impact']}/10)...")

            tg_ok = send_telegram(message)
            dc_ok = send_discord(message)

            if tg_ok or dc_ok:
                alerts_sent += 1
                gist_alerts.append({"tweet": tweet, "score": score})
                log.info(f"[Main] Alert #{alerts_sent} sent (Telegram={tg_ok}, Discord={dc_ok}).")

            # Telegram flood control
            await asyncio.sleep(TELEGRAM_RATE_LIMIT_SLEEP)

    # ── Step 4b: Narrative Detection ──────────────────────────────────────────
    narrative_msg = None
    if tweets:
        await asyncio.sleep(GROQ_RATE_LIMIT_SLEEP)
        narrative_msg = detect_narratives(tweets, groq_client, market_snapshot=snapshot, finviz_context=finviz_context)
        if narrative_msg:
            send_telegram(narrative_msg)
            send_discord(narrative_msg)

    # ── Step 4c: Polymarket Macro Markets ─────────────────────────────────────
    polymarket_data = fetch_polymarket_markets()

    # ── Step 4d: Write Gist Snapshot ──────────────────────────────────────────
    write_to_gist(
        narrative=narrative_msg,
        top_alerts=gist_alerts,
        polymarket=polymarket_data,
    )

    # ── Step 5: Summary ────────────────────────────────────────────────────────
    log.info("=" * 60)
    if alerts_sent == 0:
        log.info("[Main] No qualifying alerts this run. Markets are quiet (or threshold is high).")
    else:
        log.info(f"[Main] Done. {alerts_sent} alert(s) sent this run.")
    log.info("=" * 60)


if __name__ == "__main__":
    asyncio.run(main())
