# X ACCOUNT SETUP — re-login each run (GitHub Actions has no persistent fs)
# ─────────────────────────────────────────────────────────────────────────────

async def login_accounts(api: API) -> int:
    """
    Add up to 3 X accounts to the twscrape pool and log them in.

    Supports two auth modes per account (checked in order):
      1. Cookie auth  — set X_ACCOUNT_N_COOKIES="auth_token=xxx; ct0=yyy"
         Extracted from a browser session; bypasses the Cloudflare-protected
         login flow entirely.  Recommended for cloud/GitHub Actions environments.
      2. Password auth — set X_ACCOUNT_N_USER / PASS / EMAIL as before.

    Returns the number of accounts that are active after login, or 0 on failure.
    """
    accounts_added = 0

    for i in range(1, 4):
        username = os.getenv(f"X_ACCOUNT_{i}_USER", "").strip()
        password = os.getenv(f"X_ACCOUNT_{i}_PASS", "").strip()
        email = os.getenv(f"X_ACCOUNT_{i}_EMAIL", "").strip()
        cookies = os.getenv(f"X_ACCOUNT_{i}_COOKIES", "").strip()

        if not (username and password and email):
            continue  # Skip unconfigured slots

        try:
            await api.pool.add_account(
                username, password, email, email,
                cookies=cookies if cookies else None,
            )
            auth_mode = "cookie" if cookies else "password"
            log.info(f"[X Auth] Added account slot {i}: @{username} (mode={auth_mode})")
            accounts_added += 1
        except Exception as e:
            log.warning(f"[X Auth] Failed to add account {i} (@{username}): {e}")

    if accounts_added == 0:
        log.error("[X Auth] No valid X accounts configured. Set X_ACCOUNT_1_* secrets.")
        return 0

    try:
        await api.pool.login_all()
    except Exception as e:
        log.error(f"[X Auth] login_all() raised: {e}")
        return 0

    # Verify how many accounts are actually active — login_all() swallows
    # per-account failures (e.g. Cloudflare blocks) without raising.
    try:
        all_accts = await api.pool.get_all()
        active_count = sum(1 for a in all_accts if a.active)
    except Exception:
        # Older twscrape versions may not expose get_all(); fall back gracefully.
        log.warning("[X Auth] Could not verify active account count (twscrape version).")
        active_count = accounts_added

    if active_count == 0:
        log.error(
            "[X Auth] 0 accounts active after login — all logins failed.\n"
            "  Most likely cause: Cloudflare is blocking the password-based login\n"
            "  flow from this IP (common on GitHub Actions / cloud runners).\n"
            "  Fix: extract auth_token + ct0 cookies from a browser session and\n"
            "  set X_ACCOUNT_1_COOKIES='auth_token=<value>; ct0=<value>' in secrets."
        )
        return 0

    log.info(f"[X Auth] {active_count}/{accounts_added} account(s) active after login.")
    return active_count


# ─────────────────────────────────────────────────────────────────────────────
# TWEET SCRAPING — search X and filter to the lookback window
# ─────────────────────────────────────────────────────────────────────────────

async def scrape_tweets(api: API) -> list[dict]:
    """
    Search X using SEARCH_QUERIES and return tweets from the last LOOKBACK_MINUTES.
    Runs all thematic queries and deduplicates by tweet ID.
    Each returned dict has: id, text, url, created_at, author, likes.
    """
    cutoff = datetime.now(timezone.utc) - timedelta(minutes=LOOKBACK_MINUTES)
    log.info(f"[Scrape] Searching tweets since {cutoff.strftime('%H:%M:%S')} UTC")

    tweets = []
    seen_ids = set()

    for i, query in enumerate(SEARCH_QUERIES, 1):
        log.info(f"[Scrape] Running query {i}/{len(SEARCH_QUERIES)}")
        try:
            async for tweet in api.search(query, limit=200):
                if tweet.id in seen_ids:
                    continue

                # Filter to the lookback window
                tweet_time = tweet.date  # twscrape returns tz-aware datetime
                if tweet_time < cutoff:
                    continue

                views = tweet.viewCount or 0
                followers = tweet.user.followersCount or 0

                # Pre-LLM quality filter — skip low-quality tweets before Groq
                if views < MIN_VIEWS or followers < MIN_FOLLOWERS:
                    log.info(
                        f"[Scrape] Skipping @{tweet.user.username} | "
                        f"{views} views, {followers} followers (below threshold)"
                    )
                    continue

                tweet_data = {
                    "id": tweet.id,
                    "text": tweet.rawContent,
                    "url": f"https://x.com/{tweet.user.username}/status/{tweet.id}",
                    "created_at": tweet_time,
                    "author": tweet.user.username,
                    "likes": tweet.likeCount,
                    "views": views,
                    "followers": followers,
                }
                tweets.append(tweet_data)
                seen_ids.add(tweet.id)
                log.info(
                    f"[Scrape] Found: @{tweet.user.username} | "
                    f"{tweet.likeCount} likes | {views} views | "
                    f"{followers} followers | {tweet_time.strftime('%H:%M')} UTC"
                )
        except Exception as e:
            log.error(f"[Scrape] Search query {i} failed: {e}")

    log.info(f"[Scrape] {len(tweets)} tweet(s) total across {len(SEARCH_QUERIES)} queries.")
    return tweets


# ─────────────────────────────────────────────────────────────────────────────
# GROQ SCORING — ask LLaMA to score market impact and summarize
# ─────────────────────────────────────────────────────────────────────────────

def batch_score_tweets(tweets_batch: list[dict], groq_client: Groq, finviz_context: str | None = None) -> list[dict | None]:
    """
    Score multiple tweets in a single Groq API call to stay within free-tier limits.
    Returns a list of score dicts (or None for tweets that failed to parse),
    in the same order as the input batch.
    """
    # Build numbered tweet list for the prompt
    tweet_list = ""
    for i, t in enumerate(tweets_batch, 1):
        tweet_list += f"[{i}] @{t['author']}: {t['text'][:300]}\n"

    finviz_block = ""
    if finviz_context:
        finviz_block = f"""
Live Market Signals (from FinViz — use to cross-validate tweet claims):
{finviz_context}

"""

    prompt = f"""You are a veteran macro hedge-fund trader (10+ years). Rate each tweet for REAL market-moving impact (1-10).
{finviz_block}
{tweet_list}

Scoring rules — be ruthless:
- 8-10: ONLY verified breaking news with immediate, concrete price impact (Fed surprise, CPI miss/beat, war escalation, earnings shock, central bank pivot). Must be specific, sourced, and actionable RIGHT NOW.
- 6-7: Meaningful data or commentary with delayed impact. Notable but not urgent.
- 1-5: Opinion, speculation, hype, repeating known news, vague macro takes, excessive hashtags/cashtags.
- Default to 5 or below unless the tweet would cause a trader to act within the hour.
- Downgrade anything without a verifiable source, specific number, or named institution.
- Downgrade hype language ("trillions", "moon", "insane", "🚀🚀🚀").
- If a tweet mentions a ticker in FinViz unusual volume, +1. If it contradicts market data, -2.

Reply in EXACT JSON format with no extra text:
{{"results": [{{"id": 1, "score": <int 1-10>, "sentiment": "<Bullish OR Bearish OR Neutral>", "category": "<Commodities OR Macro OR Indices OR Bonds OR Forex OR Crypto OR Earnings>", "summary": "<one crisp actionable sentence, max 22 words>"}}, ...]}}"""

    try:
        response = groq_client.chat.completions.create(
            model=MODEL_ID,
            messages=[{"role": "user", "content": prompt}],
            response_format={"type": "json_object"},
            temperature=0.1,
            max_tokens=200 * len(tweets_batch),
        )
        raw = response.choices[0].message.content.strip()
        data = json.loads(raw)
        results_list = data.get("results", [])

        # Map results by their id (1-indexed) back to batch order
        results_by_id = {int(r.get("id", 0)): r for r in results_list}

        scores = []
        for i in range(1, len(tweets_batch) + 1):
            r = results_by_id.get(i)
            if r:
                score = {
                    "impact": int(r.get("score", 0)),
                    "sentiment": str(r.get("sentiment", "Neutral")).strip(),
                    "category": str(r.get("category", "Macro")).strip(),
                    "summary": str(r.get("summary", "")).strip(),
                }
                log.info(
                    f"[Groq] Tweet {i}: {score['impact']}/10 | "
                    f"{score['sentiment']} | {score['category']} | {score['summary'][:60]}..."
                )
                scores.append(score)
            else:
                log.warning(f"[Groq] Tweet {i}: missing from batch response.")
                scores.append(None)

        return scores

    except json.JSONDecodeError as e:
        log.warning(f"[Groq] Batch JSON parse error: {e}")
        return [None] * len(tweets_batch)
    except Exception as e:
        log.warning(f"[Groq] Batch API error: {e}")
        return [None] * len(tweets_batch)


# ─────────────────────────────────────────────────────────────────────────────
# ALERT FORMATTING — build the message sent to Telegram/Discord
# ─────────────────────────────────────────────────────────────────────────────

def build_alert_message(tweet: dict, score: dict) -> str:
    """
    Format a tweet + Groq score into a clean alert message.
    Includes sentiment, category, cashtags/hashtags.
    """
    impact = score["impact"]
    sentiment = score.get("sentiment", "Neutral")
    category = score.get("category", "Macro")
    summary = score["summary"]
    url = tweet["url"]
    ts = tweet["created_at"].strftime("%Y-%m-%d %H:%M UTC")

    # Sentiment emoji
    sent_emoji = {"Bullish": "🟢", "Bearish": "🔴"}.get(sentiment, "⚪")

    # Extract cashtags ($NVDA) and hashtags (#CPI) from tweet text
    cashtags = re.findall(r"\$[A-Z]{1,5}", tweet["text"])
    hashtags = re.findall(r"#\w+", tweet["text"])
    tags = " ".join(dict.fromkeys(cashtags + hashtags))[:100]  # dedupe, limit length
    tags_line = f"📊 {tags}" if tags else ""

    sep = "━━━━━━━━━━━━━━━━━━━━"

    lines = [
        sep,
        f"🚨 [IMPACT {impact}/10] {sent_emoji} {sentiment} | {category}",
        "",
        f"{summary}",
        "",
        f"🔗 {url}",
        f"🕒 {ts}",
    ]
    if tags_line:
        lines.append(tags_line)
    lines.append(sep)

    return "\n".join(lines)


# ─────────────────────────────────────────────────────────────────────────────
# NARRATIVE DETECTION — cross-tweet theme analysis via Groq
# ─────────────────────────────────────────────────────────────────────────────

def detect_narratives(tweets: list[dict], groq_client: Groq, market_snapshot: str | None = None, finviz_context: str | None = None) -> str | None:
    """
    Analyze all scraped tweets as a batch to identify dominant market-moving
    narratives/themes. Returns a formatted narrative summary, or None on error.
    """
    if not tweets:
        return None

    # Concatenate all tweet texts with URLs and engagement metrics, truncated to ~8000 chars
    combined = ""
    for t in tweets:
        url = t.get("url", f"https://x.com/{t['author']}/status/{t.get('id', '')}")
        likes = t.get("likes", 0)
        views = t.get("views", 0)
        followers = t.get("followers", 0)
        entry = f"@{t['author']} ({url}) [likes:{likes} views:{views} followers:{followers}]: {t['text']}\n---\n"
        if len(combined) + len(entry) > 8000:
            break
        combined += entry

    market_context = ""
    if market_snapshot:
        market_context = f"""
Current Market Data (for reference — correlate with tweet narratives):
{market_snapshot}
"""

    finviz_block = ""
    if finviz_context:
        finviz_block = f"""
Live FinViz Signals (use real sector data below instead of guessing sector performance):
{finviz_context}
"""

    prompt = f"""You are a veteran macro hedge-fund strategist. Analyze the following batch of financial tweets and identify the dominant market-moving narratives/themes.
{market_context}{finviz_block}
Tweets:
{combined}

Instructions:
- Identify 2-3 dominant narratives emerging across these tweets (e.g., AI boom, yen carry unwind, disinflation, geopolitical oil risks, rate cut expectations)
- For each narrative, state the conviction level (High/Medium/Low) and direction (Bullish/Bearish/Mixed)
- Weight tweets by engagement (likes, views) and author influence (followers) when assessing conviction
- Quote a short phrase from one tweet as evidence for each narrative
- Cite the source tweet author and URL for each piece of evidence
- If market data is provided, reference actual price moves that confirm or contradict the narrative
- Identify the single overall dominant theme
- Provide a sector heatmap (use real FinViz sector % data if available, otherwise estimate from tweets) and an overall sentiment score

Reply in this EXACT plain-text format (no markdown, no JSON):

🔍 Market Narratives

1. [Narrative Name] – Bullish/Bearish/Mixed – High/Medium/Low conviction
   Evidence: "short quote from a tweet"
   Source: @author — <tweet URL>

2. [Narrative Name] – Bullish/Bearish/Mixed – High/Medium/Low conviction
   Evidence: "short quote from a tweet"
   Source: @author — <tweet URL>

📊 Sector Heatmap
🟢 Bullish: sector1, sector2
🔴 Bearish: sector3
⚪ Neutral: sector4

🎯 Overall dominant theme: one-sentence summary of the strongest narrative
📈 Tweet Sentiment Score: X/10 (1=extreme fear, 10=extreme greed)"""

    try:
        response = groq_client.chat.completions.create(
            model=MODEL_ID,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.2,
            max_tokens=800,
        )
        result = response.choices[0].message.content.strip()
        log.info(f"[Narratives] Detected narratives ({len(result)} chars)")
        return result
    except Exception as e:
        log.warning(f"[Narratives] Groq API error: {e}")
        return None


# ─────────────────────────────────────────────────────────────────────────────
# TELEGRAM — send alert via Bot API
# ─────────────────────────────────────────────────────────────────────────────

def send_telegram(message: str) -> bool:
    """
    Send a message to the configured Telegram chat/channel.
    Returns True on success, False on failure.
    """
    token = os.getenv("TELEGRAM_BOT_TOKEN", "").strip()
    chat_id = os.getenv("TELEGRAM_CHAT_ID", "").strip()

    if not (token and chat_id):
        log.warning("[Telegram] Skipping — TELEGRAM_BOT_TOKEN or TELEGRAM_CHAT_ID not set.")
        return False

    url = f"https://api.telegram.org/bot{token}/sendMessage"
    payload = {
        "chat_id": chat_id,
        "text": message,
        "parse_mode": "HTML",
        "disable_web_page_preview": False,
    }

    try:
        response = httpx.post(url, json=payload, timeout=10)
        if response.status_code == 200:
            log.info("[Telegram] Alert sent successfully.")
            return True
        else:
            log.error(f"[Telegram] Failed ({response.status_code}): {response.text[:200]}")
            return False
    except Exception as e:
        log.error(f"[Telegram] Request error: {e}")
        return False


