# Tag IDs for rotating/daily markets — fetches top 1 by volume per tag
# Beirut (104291), Black Sea (101958)
_POLYMARKET_TAG_IDS = [104291, 101958]


def _polymarket_url(m: dict, fallback_slug: str = "") -> str:
    """Extract the correct Polymarket event URL from a market object.
    Market slugs have numeric suffixes (-484 etc) that 404 on polymarket.com/event/.
    The events[0].slug is the canonical event URL slug."""
    events = m.get("events") or []
    if events and isinstance(events[0], dict):
        event_slug = events[0].get("slug", "")
        if event_slug:
            return f"https://polymarket.com/event/{event_slug}"
    # Fallback: strip trailing -NNN suffix from market slug
    slug = m.get("slug") or fallback_slug
    import re
    clean = re.sub(r"-\d+$", "", slug)
    return f"https://polymarket.com/event/{clean}"


def _polymarket_prob(m: dict) -> float | None:
    try:
        prices = json.loads(m.get("outcomePrices", "[]"))
        return float(prices[0]) if prices else None
    except Exception:
        return None


def fetch_polymarket_markets() -> list[dict]:
    """
    Fetch macro prediction market probabilities from Polymarket's public gamma API.
    Returns list of {question, probability, volume, url} dicts, or [] on failure.
    """
    results = []
    base = "https://gamma-api.polymarket.com/markets"

    for slug in _POLYMARKET_SLUGS:
        try:
            resp = httpx.get(
                base,
                params={"slug": slug},
                timeout=8,
                headers={"User-Agent": "FinancialNewsBot/1.0"},
            )
            if not resp.is_success:
                continue
            data = resp.json()
            markets = data if isinstance(data, list) else data.get("markets", [])
            if not markets:
                continue
            m = markets[0]
            prob = _polymarket_prob(m)
            results.append({
                "question": m.get("question", slug),
                "probability": prob,
                "volume": m.get("volume", 0),
                "url": _polymarket_url(m, slug),
            })
            log.info(f"[Polymarket] {m.get('question', slug)[:60]} → {prob:.0%}" if prob else f"[Polymarket] {slug} → no prob")
        except Exception as e:
            log.warning(f"[Polymarket] Failed to fetch {slug}: {e}")

    # ── Tag-based fetch for rotating daily markets ───────────────────────────
    for tag_id in _POLYMARKET_TAG_IDS:
        try:
            resp = httpx.get(
                base,
                params={"active": "true", "closed": "false", "tag_id": tag_id, "limit": 1, "order": "volume", "ascending": "false"},
                timeout=8,
                headers={"User-Agent": "FinancialNewsBot/1.0"},
            )
            if not resp.is_success:
                continue
            markets = resp.json()
            if not markets:
                continue
            m = markets[0]
            prob = _polymarket_prob(m)
            results.append({
                "question": m.get("question", ""),
                "probability": prob,
                "volume": m.get("volume", 0),
                "url": _polymarket_url(m),
            })
            log.info(f"[Polymarket/tag={tag_id}] {m.get('question', '')[:60]} → {prob:.0%}" if prob else f"[Polymarket/tag={tag_id}] no prob")
        except Exception as e:
            log.warning(f"[Polymarket] Failed to fetch tag {tag_id}: {e}")

    log.info(f"[Polymarket] Fetched {len(results)} markets.")
    return results


def polymarket_to_text(markets: list[dict]) -> str:
    """Format Polymarket markets as plain text for AI prompt injection."""
    if not markets:
        return ""
    lines = ["Polymarket Prediction Markets:"]
    for m in markets:
        prob_str = f"{m['probability']:.0%}" if m.get("probability") is not None else "N/A"
        lines.append(f"  • {m['question']} → {prob_str}")
    return "\n".join(lines)


